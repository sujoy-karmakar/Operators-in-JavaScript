// Describe the usage of the comma operator in JavaScript and provide an example.
// Ans:-
/**
 * Comma Operator allow us to evaluate multiple expression in a single statement like declaring variables in a single statement seprating them with comma operator and return the value of the last expression. 
 */
// example-
const a = 5, b = 10, c = 15;
let result = (a, b, c);
console.log(result);
/*In this  comma operator use time we see comma operator declaring variables is a single statement and it return valu of c because c is last expression.
*/